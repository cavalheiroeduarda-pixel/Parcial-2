# Mostra uma mensagem inicial para o usuário
print("Cálculo da área de um triângulo")
# Pede ao usuário o um valor da base e converte para decimal
base = float(input("Digite o valor da base: "))
# Pede ao usuário o valor da altura e converte para decimal
altura = float(input("Digite o valor da altura: "))
# Calcula a área do triângulo usando a fórmula (base * altura) / 2
area = (base * altura) / 2
# Mostra o resultado na tela
print("A área do triângulo é:", area)
